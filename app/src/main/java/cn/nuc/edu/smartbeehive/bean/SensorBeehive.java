package cn.nuc.edu.smartbeehive.bean;

public class SensorBeehive {

    private String Bid;
    private String Beehive_Tem;
    private String Beehive_Hum;
    private String Soil_hum;
    private String Beehive_led;
    private String Light;
    private String Bee_fengshan;
    private String Bee_jidianqi;

    public String getBid() {
        return Bid;
    }

    public void setBid(String bid) {
        Bid = bid;
    }

    public String getBeehive_Tem() {
        return Beehive_Tem;
    }

    public void setBeehive_Tem(String beehive_Tem) {
        Beehive_Tem = beehive_Tem;
    }

    public String getBeehive_Hum() {
        return Beehive_Hum;
    }

    public void setBeehive_Hum(String beehive_Hum) {
        Beehive_Hum = beehive_Hum;
    }

    public String getSoil_hum() {
        return Soil_hum;
    }

    public void setSoil_hum(String soil_hum) {
        Soil_hum = soil_hum;
    }

    public String getBeehive_led() {
        return Beehive_led;
    }

    public void setBeehive_led(String beehive_led) {
        Beehive_led = beehive_led;
    }

    public String getLight() {
        return Light;
    }

    public void setLight(String light) {
        Light = light;
    }

    public String getBee_fengshan() {
        return Bee_fengshan;
    }

    public void setBee_fengshan(String bee_fengshan) {
        Bee_fengshan = bee_fengshan;
    }

    public String getBee_jidianqi() {
        return Bee_jidianqi;
    }

    public void setBee_jidianqi(String bee_jidianqi) {
        Bee_jidianqi = bee_jidianqi;
    }
}
