package cn.nuc.edu.smartbeehive;

public class MySensorBeehive {


    public int bid;
    public int sid;
    public int beeCount ;
    public int beeActivity ;
    public int beehiveHum ;
    public float beehiveTem;
    public int beehiveLed;
    public float beeSurvivalrate;
    public float beehiveCo2 ;
    public float beehiveWeight;

    public int getBid() {
        return bid;
    }

    public void setBid(int bid) {
        this.bid = bid;
    }

    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }

    public int getBeeCount() {
        return beeCount;
    }

    public void setBeeCount(int beeCount) {
        this.beeCount = beeCount;
    }

    public int getBeeActivity() {
        return beeActivity;
    }

    public void setBeeActivity(int beeActivity) {
        this.beeActivity = beeActivity;
    }

    public int getBeehiveHum() {
        return beehiveHum;
    }

    public void setBeehiveHum(int beehiveHum) {
        this.beehiveHum = beehiveHum;
    }

    public float getBeehiveTem() {
        return beehiveTem;
    }

    public void setBeehiveTem(float beehiveTem) {
        this.beehiveTem = beehiveTem;
    }

    public int getBeehiveLed() {
        return beehiveLed;
    }

    public void setBeehiveLed(int beehiveLed) {
        this.beehiveLed = beehiveLed;
    }

    public float getBeeSurvivalrate() {
        return beeSurvivalrate;
    }

    public void setBeeSurvivalrate(float beeSurvivalrate) {
        this.beeSurvivalrate = beeSurvivalrate;
    }

    public float getBeehiveCo2() {
        return beehiveCo2;
    }

    public void setBeehiveCo2(float beehiveCo2) {
        this.beehiveCo2 = beehiveCo2;
    }

    public float getBeehiveWeight() {
        return beehiveWeight;
    }

    public void setBeehiveWeight(float beehiveWeight) {
        this.beehiveWeight = beehiveWeight;
    }
}
